// usedQuestions[cat][v] = مصفوفة بـ indices المستخدمة
const usedQuestions = {};
function getQuestion(cat, v) {
    if (!usedQuestions[cat]) usedQuestions[cat] = {};
    if (!usedQuestions[cat][v]) usedQuestions[cat][v] = [];
    const pool = questionBank[cat][v];
    const available = pool.map((_, i) => i).filter(i => !usedQuestions[cat][v].includes(i));
    if (available.length === 0) {
        // أعد تشغيل القائمة إذا انتهت
        usedQuestions[cat][v] = [];
        return pool[Math.floor(Math.random() * pool.length)];
    }
    const idx = available[Math.floor(Math.random() * available.length)];
    usedQuestions[cat][v].push(idx);
    return pool[idx];
}

